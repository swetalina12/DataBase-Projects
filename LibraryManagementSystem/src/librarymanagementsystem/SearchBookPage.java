    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
         conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/", "root", "");
         Statement stmt = conn.createStatement();
         stmt.execute("use library;");
         
          //If no value is entered 
         if(jT_BookID.getText().equals("")&& jT_BookTitle.getText().equals("")&& jT_Author.getText().equals(""))
             JOptionPane.showMessageDialog(null, "Please enter some values");
         
         else
         {//Take all values of textboxes
         tbook_id=jT_BookID.getText();
         ttitle=jT_BookTitle.getText();
         tauthor =jT_Author.getText();
         rs = stmt.executeQuery("SELECT B.BOOK_ID , TITLE , AUTHOR_NAME ,BRANCH_ID,No_of_copies,\n" +
                "No_of_copies-(select count(*) from BOOK_LOANS BL where BL.book_id=B.BOOK_ID and BRANCH_ID =BOOK_COPIES.BRANCH_ID and Date_in='NULL')\n" +
                "as Available \n" +
                "FROM BOOK_COPIES NATURAL JOIN BOOK AS A NATURAL JOIN BOOK_AUTHORS AS B \n" +
                "WHERE A.BOOK_ID = B.BOOK_ID AND B.BOOK_ID like '%"+tbook_id+"%' AND AUTHOR_NAME like '%"+tauthor+"%' AND TITLE like '%"+ttitle+"%';");
            for(int k=0;k<i;k++){
            op_table.setValueAt("", k, 0);
            op_table.setValueAt("", k, 1);
            op_table.setValueAt("", k, 2);
            op_table.setValueAt("", k, 3);
            op_table.setValueAt("", k, 4);
            op_table.setValueAt("", k, 5);
         }
         System.out.print(i);
          i=0;
         while (rs.next()) {
             //get the result set
            tableid = rs.getString("BOOK_ID");
            tabletitle= rs.getString("TITLE");
            tableauthor= rs.getString("AUTHOR_NAME");
            tablebranchid= rs.getInt("BRANCH_ID");
            tabletotalcopies=rs.getInt("NO_OF_COPIES");
            tableavlcopies=rs.getInt("Available");;

            //populate table
            op_table.setValueAt(tableid, i, 0);
            op_table.setValueAt(tabletitle, i, 1);
            op_table.setValueAt(tableauthor, i, 2);
            op_table.setValueAt(tablebranchid, i, 3);
            op_table.setValueAt(tabletotalcopies, i, 4);
            op_table.setValueAt(tableavlcopies, i, 5);
            i++;
         }}
          JOptionPane.showMessageDialog(null, i); 
          rowno=i;
          //if no match found
         if(i==0)
         JOptionPane.showMessageDialog(null, "No match found");   
         
      
        }
        catch(SQLException ex){
        System.out.println("Error in connection: " + ex.getMessage());}
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

